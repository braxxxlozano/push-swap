#include "../../inc/libft.h"

/* DESCRIPTION:
Takes as a parameter a pointer to the first element of a list and returns the
last element of the list.
---------------------------------------------------------------------------- */
t_list	*ft_lstlast(t_list *lst)
{
	if (lst == NULL)
		return (NULL);
	while (lst->next)
		lst = lst->next;
	return (lst);
}
